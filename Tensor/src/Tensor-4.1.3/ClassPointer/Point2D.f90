	subroutine Point2D_All_Value(length,inA,dim1,dim2,outp)
		integer::dim1,dim2,length
		class(*),target::inA(:)
		class(*),pointer::outp(:,:)
		if((dim1*dim2).ne.length)then
			call writemess('ERROR in pointing ',-1)
			call error_stop
		end if
		if(ClassSize(inA).lt.length)then
			call writemess('ERROR in pointing ',-1)
			call error_stop
		end if
		select type (inA)
			type is (character(len=*))
				call Point2D_All_Value_char(length,inA,dim1,dim2,outp)
			class default
				outp(1:dim1,1:dim2)=>inA(1:length)
		end select
		return
	end subroutine
