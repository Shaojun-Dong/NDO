			call writemess('dim(2)='+dim(2),-1)
			call error_Stop
		end if
		call ReshapCopya2(A,B%Data%ClassData,B%getTotalData(),B%getCharacterlen())
		return
	end subroutine

	subroutine ReshapCopya2(A,B,length,chalen)
		integer,intent(in)::length,chalen
		character(len=*)::A(length)
		character(len=chalen),pointer::ap(:)
		class(*)::B(:)
		select type(B)
			type is (character(len=*))
				ap=>B
				A=ap(1:length)
			class default
				call FastCopyArray(A,B,length)
		end select
		return
	end subroutine

